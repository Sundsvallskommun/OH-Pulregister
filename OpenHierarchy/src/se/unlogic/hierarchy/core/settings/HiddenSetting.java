package se.unlogic.hierarchy.core.settings;

public class HiddenSetting extends SingleValueNoAlternativesSetting {

	public HiddenSetting(String id, String name, String description, String value) {

		this(id, name, description, value, false);
	}
	
	public HiddenSetting(String id, String name, String description, String value, boolean required) {

		super(id, name, description, FormElement.HIDDEN, value, required, false, null);
	}

}
